package reCapDemo;

public class ReCapDortIslem {
    public int Toplama(int sayi1,int sayi2){
        return sayi1+sayi2;
    }

    public int Çıkarma(int sayi1,int sayi2){
        return sayi1-sayi2;
    }

    public int Çarp(int sayi1,int sayi2){
        return sayi1*sayi2;
    }

    public int Bölme(int sayi1,int sayi2){
        return sayi1/sayi2;
    }

}